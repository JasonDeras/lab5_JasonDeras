    private void jmi_CreacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jmi_CreacionActionPerformed
        // TODO add your handling code here:
        jd_Creacion_Usuario.setModal(true);
        jd_Creacion_Usuario.pack();
        jd_Creacion_Usuario.setLocationRelativeTo(this);
        jd_Creacion_Usuario.setVisible(true);
        int edad = 0;
        try {
            edad = (int) (((new Date()).getTime()) - jd_Fecha_nace.getDate().getTime() / (1000 * 60 * 60 * 24));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ocurrio un error y no se crreo el usuario");
        }
        Usuarios us = new Usuarios(pf_Contraseña1.getText(), tf_Nombre.getText(), tf_Apellido.getText(), tf_NUsuario1.getText(), edad, c, jd_Fecha_nace.getDate());
        usuario.add(us);
        tf_Apellido.setText("");
        tf_NUsuario1.setText("");
        tf_Nombre.setText("");
        pf_Contraseña1.setText("");
        jd_Fecha_nace.setDate(new Date());
    }//GEN-LAST:event_jmi_CreacionActionPerformed

